static  void  InitSoftware(void)
{
  InitPackUnpack();       //初始化PackUnpack模块
  InitProcHostCmd();      //初始化ProcHostCmd模块
  InitSendDataToHost();   //初始化SendDataToHost模块
  InitRoute();            //初始化Route模块
}

/*********************************************************************************************************
* 函数名称：InitHardware
* 函数功能：所有的硬件相关的模块初始化函数都放在此函数中
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月11日
* 注    意：
*********************************************************************************************************/
static  void  InitHardware(void)
{  
  SystemInit();       //系统初始化
  InitRCC();          //初始化RCC模块
  InitNVIC();         //初始化NVIC模块
  InitUART1(9600);    //初始化UART模块
  InitTimer();        //初始化Timer模块
  InitLED();          //初始化LED模块
  InitSysTick();      //初始化SysTick模块
  //InitDAC();          //初始化DAC模块
  InitADC();          //初始化ADC模块
  InitLORA();         //初始化LORA模块
}

/*********************************************************************************************************
* 函数名称：Proc2msTask
* 函数功能：2ms处理任务 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月11日
* 注    意：
*********************************************************************************************************/
static  void  Proc2msTask(void)
{  
  uint8  uart1RecData; //串口数据
  uint16 adcData;      //队列数据
  uint8  waveData;     //波形数据

  static uint16 s_iCnt4 = 0;   //计数器
  static uint8 s_iPointCnt = 0;        //温度数据包的点计数器
  static uint8 s_arrWaveData[5] = {0}; //初始化数组
  
  if(Get2msFlag())  //判断2ms标志状态
  {
    if(ReadUART1(&uart1RecData, 1)) //读串口接收数据
    {       
      ProcHostCmd(uart1RecData);  //处理命令      
    }

    s_iCnt4++;      //计数增加
    if(s_iCnt4 >= 400)  //达到800ms
    {
      if(ReadADCBuf(&adcData))  //从缓存队列中取出1个数据到adcData
      {
        waveData = (1.42 - adcData * 3.3 / 4096)*1000/4.35 + 25;  //计算获取温度的值，12位ADC，2^12=4095，参考电压3.3V
        s_arrWaveData[s_iPointCnt] = waveData;  //存放到数组
        s_iPointCnt++;  //温度数据包的点计数器加1操作

        if(s_iPointCnt >= 5)  //接收到5个点
        {
          s_iPointCnt = 0;  //计数器清零
        }
      }
      s_iCnt4 = 0;  //准备下次的循环
    }
    
    LEDFlicker(250);//调用闪烁函数     
    Clr2msFlag();   //清除2ms标志
  }
}

/*********************************************************************************************************
* 函数名称：Proc1SecTask
* 函数功能：1s处理任务 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月11日
* 注    意：
*********************************************************************************************************/
static  void  Proc1SecTask(void)
{ 
  static uint8 s_iCnt = 0;   //计数器
  if(Get1SecFlag()) //判断1s标志状态
  {
    //printf发送到UART1，即到LOAR模块
    //printf("This is the first STM32F103 Project, by Zhangsan\r\n");
    s_iCnt++;
    
    if(s_iCnt >= 2)
    {
      s_iCnt = 0;
      RouteTimerTasks();
    }
    
    Clr1SecFlag();  //清除1s标志
  }    
}

/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 创建日期：2021年07月11日
* 注    意：
*********************************************************************************************************/
int main(void)
{ 
  InitHardware();   //初始化硬件相关函数
  InitSoftware();   //初始化软件相关函数
  
  printf("Init System has been finished.\r\n" );  //打印系统状态

  while(1)
  {
    Proc2msTask();  //2ms处理任务
    Proc1SecTask(); //1s处理任务   
  }
}
